# Δημιουργήστε μια ακεραία μεταβλητη μεγεθούς λέξης και αρχικοποιιστε με 10
#Τυπώστε την μεταβλήτη
.data
Variable: .word 10
.text
la $t1,Variable #$t1=Διευθυνση της variable
lw $t2,0($t1) # $t2=πεξηερόκελν ηεο Variable=10
li $v0,1 # τυπώσε έναν ακεραίο
move $a0,$t2 #ο ακεραιος πρεπει να είναι στο $α0 για να τυπώθει
syscall
li $v0,10
syscall
